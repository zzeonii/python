from math import pow

print("%d\n"%(3 + 4))
print("%d\n"%(3 - 4))
print("%d\n"%(3 * 4))
print("%d\n"%(3 // 4))
print("%lf\n"%(3 / 4.0))
print("%d\n"%(3 % 4))
print("%lf\n"%pow(3,4))

print("%d\n"%(3 > 4))
print("%d\n"%(3 >= 4))
print("%d\n"%(3 < 4))
print("%d\n"%(3 <= 4))
print("%r\n"%(3 == 4))
print("%s\n"%(3 != 4)) 