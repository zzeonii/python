#for-in문 활용
for i in [1,2,3,4]: i
for i in range(5) : i
for i in (1,2,3,4): i
for i in {'a':1,'b':2}: i
for i in {'a':1,'b':2}.keys(): i
for i in {'a':1,'b':2}.values(): i
for i in {'a':1,'b':2}.items(): i
for i in [1,2,3,4][:]: i
for i in [1,2,3,4][:3]: i

